/// Current bridge adapter command/event protocol version.
pub const PROTOCOL_VERSION: u16 = 3;

/// Version metadata returned by health/status endpoints.
#[derive(Clone, Debug, PartialEq, Eq, Serialize, Deserialize)]
pub struct ProtocolInfo {
    /// Bridge adapter protocol version.
    pub protocol_version: u16,
    /// Linked `inline-client` crate version.
    pub client_version: String,
}

impl ProtocolInfo {
    /// Returns protocol metadata for the currently linked adapter/client.
    pub fn current() -> Self {
        Self {
            protocol_version: PROTOCOL_VERSION,
            client_version: inline_client::VERSION.to_owned(),
        }
    }
}

/// Adapter health response.
#[derive(Clone, Debug, PartialEq, Eq, Serialize, Deserialize)]
pub struct SidecarHealth {
    /// Whether the adapter process is reachable.
    pub ok: bool,
    /// Versioned adapter protocol information.
    pub protocol: ProtocolInfo,
    /// Current observed Inline client status.
    pub status: ClientStatus,
}

/// Status snapshot exposed through the bridge adapter protocol.
#[derive(Clone, Debug, PartialEq, Eq, Serialize, Deserialize)]
pub struct SidecarStatus {
    /// Versioned adapter protocol information.
    pub protocol: ProtocolInfo,
    /// Current Inline client status.
    pub status: ClientStatus,
    /// Optional redacted failure.
    pub failure: Option<ClientFailure>,
}

impl SidecarStatus {
    /// Creates a status snapshot from the current client handle state.
    pub fn current(status: ClientStatus, failure: Option<ClientFailure>) -> Self {
        Self {
            protocol: ProtocolInfo::current(),
            status,
            failure,
        }
    }

    /// Converts a native client status snapshot into the adapter protocol form.
    pub fn from_client(snapshot: inline_client::ClientStatusSnapshot) -> Self {
        Self::current(snapshot.status, snapshot.failure)
    }
}

/// Stable request ID supplied by the Go bridge or generated by HTTP wrappers.
#[derive(Clone, PartialEq, Eq, Hash, Serialize, Deserialize)]
#[serde(transparent)]
pub struct SidecarRequestId(String);

impl SidecarRequestId {
    /// Maximum request ID length in bytes.
    pub const MAX_LEN: usize = 128;

    /// Creates a validated request ID.
    pub fn try_new(value: impl AsRef<str>) -> Result<Self, RequestIdError> {
        let trimmed = value.as_ref().trim();
        if trimmed.is_empty() {
            return Err(RequestIdError::Empty);
        }
        if trimmed.len() > Self::MAX_LEN {
            return Err(RequestIdError::TooLong {
                len: trimmed.len(),
                max: Self::MAX_LEN,
            });
        }
        if trimmed.chars().any(|ch| ch.is_ascii_control()) {
            return Err(RequestIdError::ControlCharacter);
        }
        Ok(Self(trimmed.to_owned()))
    }

    /// Returns the request ID string.
    pub fn as_str(&self) -> &str {
        &self.0
    }
}

impl fmt::Debug for SidecarRequestId {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        f.debug_tuple("SidecarRequestId").field(&self.0).finish()
    }
}

impl fmt::Display for SidecarRequestId {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        self.0.fmt(f)
    }
}

impl FromStr for SidecarRequestId {
    type Err = RequestIdError;

    fn from_str(value: &str) -> Result<Self, Self::Err> {
        Self::try_new(value)
    }
}

/// Request ID validation error.
#[derive(Clone, Debug, PartialEq, Eq, thiserror::Error)]
pub enum RequestIdError {
    /// Request ID was empty or whitespace-only.
    #[error("request ID must not be empty")]
    Empty,
    /// Request ID exceeded the public protocol limit.
    #[error("request ID is too long ({len} > {max})")]
    TooLong {
        /// Actual length in bytes.
        len: usize,
        /// Maximum allowed length in bytes.
        max: usize,
    },
    /// Request ID contained ASCII control characters.
    #[error("request ID must not contain ASCII control characters")]
    ControlCharacter,
}

/// Versioned adapter command request.
#[derive(Clone, Debug, PartialEq, Eq, Serialize, Deserialize)]
pub struct SidecarRequest {
    /// Adapter protocol version.
    pub protocol_version: u16,
    /// Request ID supplied by the caller.
    pub id: SidecarRequestId,
    /// Command payload.
    pub command: SidecarCommand,
}

impl SidecarRequest {
    /// Creates a request using the current protocol version.
    pub fn current(id: SidecarRequestId, command: SidecarCommand) -> Self {
        Self {
            protocol_version: PROTOCOL_VERSION,
            id,
            command,
        }
    }
}

/// Adapter command payload.
#[derive(Clone, Debug, PartialEq, Eq, Serialize, Deserialize)]
#[serde(tag = "type", content = "data", rename_all = "snake_case")]
#[non_exhaustive]
pub enum SidecarCommand {
    /// Send an Inline login code.
    AuthStart(AuthStartRequest),
    /// Verify an Inline login code and persist the resulting session.
    AuthVerify(AuthVerifyRequest),
    /// Resume a previously stored session, if available.
    Resume,
    /// Connect or reconnect the client.
    Connect(ConnectRequest),
    /// Logout the current account.
    Logout,
    /// Fetch current status.
    Status,
    /// Fetch dialogs.
    Dialogs(DialogsRequest),
    /// Fetch only durable cached dialogs.
    CachedDialogs(DialogsRequest),
    /// Fetch account-level durable reconciliation state.
    AccountState,
    /// Fetch durable reconciliation state for one chat.
    ChatState(ChatStateRequest),
    /// Fetch history for a chat.
    History(HistoryRequest),
    /// Fetch only durable cached history for reconciliation.
    CachedHistory(HistoryRequest),
    /// Fetch participants for a chat.
    ChatParticipants(ChatParticipantsRequest),
    /// Add a participant to a chat.
    AddChatParticipant(AddChatParticipantRequest),
    /// Remove a participant from a chat.
    RemoveChatParticipant(RemoveChatParticipantRequest),
    /// Update mutable chat metadata.
    UpdateChatInfo(UpdateChatInfoRequest),
    /// Delete a chat when the authenticated user is allowed to.
    DeleteChat(DeleteChatRequest),
    /// Create or open a direct message chat.
    CreateDm(CreateDmRequest),
    /// Create a regular Inline thread chat.
    CreateThread(CreateThreadRequest),
    /// Create a child/reply Inline thread chat.
    CreateReplyThread(CreateReplyThreadRequest),
    /// Send a text message.
    SendText(SendTextRequest),
    /// Edit a message.
    EditMessage(EditMessageRequest),
    /// Delete or unsend a message.
    DeleteMessage(DeleteMessageRequest),
    /// Add or remove a reaction.
    React(ReactRequest),
    /// Mark messages read.
    Read(ReadRequest),
    /// Set explicit marked-unread state.
    SetMarkedUnread(SetMarkedUnreadRequest),
    /// Set or clear a per-dialog notification override.
    UpdateDialogNotifications(UpdateDialogNotificationsRequest),
    /// Set typing state.
    Typing(TypingRequest),
    /// Upload and send media.
    Upload(UploadRequest),
}

impl SidecarCommand {
    /// Returns the stable command kind name.
    pub const fn kind(&self) -> &'static str {
        match self {
            Self::AuthStart(_) => "auth_start",
            Self::AuthVerify(_) => "auth_verify",
            Self::Resume => "resume",
            Self::Connect(_) => "connect",
            Self::Logout => "logout",
            Self::Status => "status",
            Self::Dialogs(_) => "dialogs",
            Self::CachedDialogs(_) => "cached_dialogs",
            Self::AccountState => "account_state",
            Self::ChatState(_) => "chat_state",
            Self::History(_) => "history",
            Self::CachedHistory(_) => "cached_history",
            Self::ChatParticipants(_) => "chat_participants",
            Self::AddChatParticipant(_) => "add_chat_participant",
            Self::RemoveChatParticipant(_) => "remove_chat_participant",
            Self::UpdateChatInfo(_) => "update_chat_info",
            Self::DeleteChat(_) => "delete_chat",
            Self::CreateDm(_) => "create_dm",
            Self::CreateThread(_) => "create_thread",
            Self::CreateReplyThread(_) => "create_reply_thread",
            Self::SendText(_) => "send_text",
            Self::EditMessage(_) => "edit_message",
            Self::DeleteMessage(_) => "delete_message",
            Self::React(_) => "react",
            Self::Read(_) => "read",
            Self::SetMarkedUnread(_) => "set_marked_unread",
            Self::UpdateDialogNotifications(_) => "update_dialog_notifications",
            Self::Typing(_) => "typing",
            Self::Upload(_) => "upload",
        }
    }
}

