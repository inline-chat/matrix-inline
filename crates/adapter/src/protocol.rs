/// Successful adapter result payload.
#[derive(Clone, Debug, PartialEq, Eq, Serialize, Deserialize)]
#[serde(tag = "type", content = "data", rename_all = "snake_case")]
#[non_exhaustive]
pub enum SidecarResult {
    /// No payload.
    Empty,
    /// Protocol information.
    ProtocolInfo(ProtocolInfo),
    /// Status snapshot.
    Status(SidecarStatus),
    /// Auth start result.
    AuthStart(AuthStartResult),
    /// Auth verify result.
    AuthVerify(AuthVerifySidecarResult),
    /// Dialog page.
    Dialogs(DialogsPage),
    /// Account-level durable reconciliation state.
    AccountState(AccountStateSnapshot),
    /// Durable reconciliation state for one chat.
    ChatState(Box<ChatStateSnapshot>),
    /// History page.
    History(HistoryPage),
    /// Chat participant snapshot.
    ChatParticipants(ChatParticipantsPage),
    /// Created or opened chat.
    CreatedChat(CreatedChat),
    /// Message mutation acknowledgement.
    Message(MessageMutation),
    /// Upload handle.
    Upload(UploadHandle),
}

/// Redacted adapter error payload.
#[derive(Clone, Debug, PartialEq, Eq, Serialize, Deserialize)]
pub struct SidecarError {
    /// Stable error category.
    pub category: ClientErrorCategory,
    /// Redacted message suitable for status output.
    pub message: String,
    /// Optional retry delay in seconds.
    pub retry_after_seconds: Option<u64>,
}

impl SidecarError {
    /// Creates a redacted adapter error.
    pub fn new(category: ClientErrorCategory, message: impl Into<String>) -> Self {
        Self {
            category,
            message: message.into(),
            retry_after_seconds: None,
        }
    }
}

impl From<ClientFailure> for SidecarError {
    fn from(failure: ClientFailure) -> Self {
        Self::new(failure.category, failure.message)
    }
}

impl From<BackendError> for SidecarError {
    fn from(error: BackendError) -> Self {
        Self {
            category: error.category,
            message: error.message,
            retry_after_seconds: error.retry_after_seconds,
        }
    }
}

/// Adapter command outcome.
#[derive(Clone, Debug, PartialEq, Eq, Serialize, Deserialize)]
#[serde(tag = "status", content = "data", rename_all = "snake_case")]
pub enum SidecarOutcome {
    /// Successful result.
    Ok(SidecarResult),
    /// Failed result.
    Error(SidecarError),
}

/// Versioned adapter command response.
#[derive(Clone, Debug, PartialEq, Eq, Serialize, Deserialize)]
pub struct SidecarResponse {
    /// Adapter protocol version.
    pub protocol_version: u16,
    /// Request ID this response belongs to.
    pub id: SidecarRequestId,
    /// Command outcome.
    pub outcome: SidecarOutcome,
}

impl SidecarResponse {
    /// Creates a successful response using the current protocol version.
    pub fn ok(id: SidecarRequestId, result: SidecarResult) -> Self {
        Self {
            protocol_version: PROTOCOL_VERSION,
            id,
            outcome: SidecarOutcome::Ok(result),
        }
    }

    /// Creates an error response using the current protocol version.
    pub fn error(id: SidecarRequestId, error: SidecarError) -> Self {
        Self {
            protocol_version: PROTOCOL_VERSION,
            id,
            outcome: SidecarOutcome::Error(error),
        }
    }
}

/// Versioned adapter event envelope.
#[derive(Clone, PartialEq, Eq, Serialize, Deserialize)]
pub struct SidecarEventEnvelope {
    /// Adapter protocol version.
    pub protocol_version: u16,
    /// Account/store namespace that owns this event sequence.
    pub session_namespace: String,
    /// Optional monotonically increasing adapter-local sequence.
    pub sequence: Option<u64>,
    /// Event delivery reliability class.
    pub reliability: EventReliability,
    /// Committed native client event.
    pub event: ClientEvent,
}

impl fmt::Debug for SidecarEventEnvelope {
    fn fmt(&self, formatter: &mut fmt::Formatter<'_>) -> fmt::Result {
        formatter
            .debug_struct("SidecarEventEnvelope")
            .field("protocol_version", &self.protocol_version)
            .field("session_namespace", &"[redacted]")
            .field("sequence", &self.sequence)
            .field("reliability", &self.reliability)
            .field("event", &self.event)
            .finish()
    }
}

impl SidecarEventEnvelope {
    /// Creates an event envelope using the current protocol version.
    pub fn current(event: ClientEvent) -> Self {
        Self {
            protocol_version: PROTOCOL_VERSION,
            session_namespace: String::new(),
            sequence: None,
            reliability: event.reliability(),
            event,
        }
    }

    /// Sets the adapter-local sequence.
    pub fn with_sequence(mut self, sequence: u64) -> Self {
        self.sequence = Some(sequence);
        self
    }

    /// Sets the account/store namespace for replay isolation.
    pub fn with_session_namespace(mut self, namespace: impl Into<String>) -> Self {
        self.session_namespace = namespace.into();
        self
    }
}

#[cfg(test)]
mod tests {
    use inline_client::{ExternalId, PeerRef, RandomId};

    use super::*;

    #[test]
    fn request_round_trips_as_tagged_json() {
        let req = SidecarRequest::current(
            SidecarRequestId::try_new("req-1").unwrap(),
            SidecarCommand::SendText(SendTextRequest {
                peer: PeerRef::Chat {
                    chat_id: InlineId::new(7),
                },
                text: "hello".to_owned(),
                external_id: Some(ExternalId::try_new("matrix-event", "$event").unwrap()),
                random_id: Some(RandomId::new(99)),
                reply_to_message_id: None,
            }),
        );

        let json = serde_json::to_value(&req).unwrap();
        assert_eq!(json["protocol_version"], PROTOCOL_VERSION);
        assert_eq!(json["command"]["type"], "send_text");

        let decoded: SidecarRequest = serde_json::from_value(json).unwrap();
        assert_eq!(decoded, req);
    }

    #[test]
    fn event_uses_client_event_reliability() {
        let event = ClientEvent::Typing {
            chat_id: InlineId::new(1),
            user_id: InlineId::new(2),
            is_typing: true,
        };

        let envelope = SidecarEventEnvelope::current(event)
            .with_session_namespace("secret-namespace")
            .with_sequence(10);

        assert_eq!(envelope.protocol_version, PROTOCOL_VERSION);
        assert_eq!(envelope.sequence, Some(10));
        assert_eq!(envelope.reliability, EventReliability::BestEffort);
        let rendered = format!("{envelope:?}");
        assert!(rendered.contains("[redacted]"));
        assert!(!rendered.contains("secret-namespace"));
    }

    #[test]
    fn response_error_wraps_client_failure() {
        let failure = ClientFailure::new(ClientErrorCategory::AuthExpired, "relogin required");
        let response = SidecarResponse::error(
            SidecarRequestId::try_new("req-2").unwrap(),
            SidecarError::from(failure),
        );

        match response.outcome {
            SidecarOutcome::Error(error) => {
                assert_eq!(error.category, ClientErrorCategory::AuthExpired);
                assert_eq!(error.message, "relogin required");
            }
            SidecarOutcome::Ok(_) => panic!("expected error outcome"),
        }
    }

    #[test]
    fn backend_error_preserves_typed_retry_hint() {
        let error = BackendError::new(ClientErrorCategory::RateLimited, "slow down")
            .with_retry_after_seconds(42);

        let sidecar = SidecarError::from(error);

        assert_eq!(sidecar.category, ClientErrorCategory::RateLimited);
        assert_eq!(sidecar.retry_after_seconds, Some(42));
    }
}
